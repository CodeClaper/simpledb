/* Leftmost 32bit word postion. */
int leftmost_32_pos(uint32_t word) {
    int shift = 32 - 8;
    Assert(word != 0);
    while ((word >> shift) == 0)
        shift -=8;
    return shift + leftmost_one_pos[(word >> shift) & 255];
}
